<section class="policy-page" aria-labelledby="policy-title">
	<h1 id="policy-title" class="policy-title">Damage Management Policy</h1>

	<div class="policy-content">
		<section class="policy-block">
			<h2 class="policy-heading">1. Initial Vehicle Inspection</h2>
			<p class="policy-paragraph">Before a rental begins, the Ridex Agent and the renter perform a physical check of the vehicle. All existing scratches, dents, or mechanical conditions are recorded to establish the baseline condition of the vehicle at the time of handover.</p>
		</section>

		<section class="policy-block">
			<h2 class="policy-heading">2. Renter Responsibility</h2>
			<p class="policy-paragraph">Renters maintain full custody of the vehicle during the "On Trip" status. The vehicle is returned in the condition documented during the initial pickup.</p>
		</section>

		<section class="policy-block">
			<h2 class="policy-heading">3. Incident Management</h2>
			<p class="policy-paragraph">In the event of an accident or damage, the renter coordinates with the Ridex Agent to manage the situation. This ensures that the vehicle status is accurately updated for safety and future availability.</p>
		</section>

		<section class="policy-block">
			<h2 class="policy-heading">4. Post-Trip Inspection</h2>
			<p class="policy-paragraph">Upon return, the Ridex Agent conducts a final inspection. Any new damages identified during this process are documented and compared against the initial pickup records.</p>
		</section>

		<section class="policy-block">
			<h2 class="policy-heading">5. Settlement of Costs</h2>
			<p class="policy-paragraph">Expenses related to repairs, parts replacement, or specialized maintenance are finalized with the Ridex Agent during the return process. These charges are added to the final billing summary, which includes the base rental rate, the 13% VAT, and any applicable late fees.</p>
		</section>

		<section class="policy-block">
			<h2 class="policy-heading">6. Major Incidents</h2>
			<p class="policy-paragraph">For significant damages or vehicle security issues, GPS Tracking data is utilized to assist in recovery and reporting. Final resolutions for major incidents are managed in coordination with the Ridex Administration and the relevant authorities in Kathmandu.</p>
		</section>
	</div>
</section>
