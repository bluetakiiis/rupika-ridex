<?php
/**
 * Purpose: Export all DB tables into a tracked SQL snapshot for deployment parity.
 */

require_once __DIR__ . '/../config/database.php';

$defaultOutputPath = APP_ROOT . '/data/deploy/db_snapshot.sql';
$outputPath = $defaultOutputPath;

$args = array_slice($_SERVER['argv'] ?? [], 1);
foreach ($args as $arg) {
	if ($arg === '--help' || $arg === '-h') {
		echo 'Usage: php bin/export_db_snapshot.php [--output=path]' . PHP_EOL;
		echo 'Exports all database tables into a deterministic SQL snapshot file.' . PHP_EOL;
		exit(0);
	}

	if (str_starts_with($arg, '--output=')) {
		$customPath = trim((string) substr($arg, 9));
		if ($customPath !== '') {
			$outputPath = str_starts_with($customPath, '/') || str_starts_with($customPath, '\\') || preg_match('/^[A-Za-z]:[\\\\\/]/', $customPath)
				? $customPath
				: APP_ROOT . '/' . ltrim(str_replace('\\', '/', $customPath), '/');
		}
	}
}

$quoteIdentifier = static function (string $identifier): string {
	return '`' . str_replace('`', '``', $identifier) . '`';
};

$toSqlLiteral = static function (PDO $pdo, mixed $value): string {
	if ($value === null) {
		return 'NULL';
	}

	if (is_bool($value)) {
		return $value ? '1' : '0';
	}

	if (is_int($value) || is_float($value)) {
		return (string) $value;
	}

	return $pdo->quote((string) $value);
};

try {
	$pdo = db();

	$tableRows = $pdo->query('SHOW TABLES')->fetchAll(PDO::FETCH_NUM);
	$tables = [];
	foreach ($tableRows as $tableRow) {
		$tableName = trim((string) ($tableRow[0] ?? ''));
		if ($tableName !== '') {
			$tables[] = $tableName;
		}
	}

	sort($tables, SORT_NATURAL | SORT_FLAG_CASE);
	if (empty($tables)) {
		throw new RuntimeException('No database tables found to export.');
	}

	$lines = [];
	$lines[] = '-- RIDEX data snapshot';
	$lines[] = '-- Generated by bin/export_db_snapshot.php';
	$lines[] = 'SET NAMES utf8mb4;';
	$lines[] = 'SET FOREIGN_KEY_CHECKS = 0;';
	$lines[] = '';

	foreach ($tables as $tableName) {
		$lines[] = 'TRUNCATE TABLE ' . $quoteIdentifier($tableName) . ';';
	}
	$lines[] = '';

	$totalRows = 0;
	foreach ($tables as $tableName) {
		$columnRows = $pdo->query('SHOW COLUMNS FROM ' . $quoteIdentifier($tableName))->fetchAll(PDO::FETCH_ASSOC);
		$columns = [];
		$primaryKeyColumns = [];
		foreach ($columnRows as $columnRow) {
			$columnName = (string) ($columnRow['Field'] ?? '');
			if ($columnName === '') {
				continue;
			}

			$columns[] = $columnName;
			if ((string) ($columnRow['Key'] ?? '') === 'PRI') {
				$primaryKeyColumns[] = $columnName;
			}
		}

		if (empty($columns)) {
			continue;
		}

		$orderColumns = !empty($primaryKeyColumns)
			? $primaryKeyColumns
			: (in_array('id', $columns, true) ? ['id'] : []);
		$orderSql = '';
		if (!empty($orderColumns)) {
			$orderSql = ' ORDER BY ' . implode(', ', array_map($quoteIdentifier, $orderColumns));
		}

		$rows = $pdo->query('SELECT * FROM ' . $quoteIdentifier($tableName) . $orderSql)->fetchAll(PDO::FETCH_ASSOC);
		if (empty($rows)) {
			continue;
		}

		$lines[] = '-- Table: ' . $tableName;
		$columnListSql = implode(', ', array_map($quoteIdentifier, $columns));
		$valueRows = [];
		$chunkSize = 200;

		foreach ($rows as $row) {
			$rowValues = [];
			foreach ($columns as $columnName) {
				$rowValues[] = $toSqlLiteral($pdo, $row[$columnName] ?? null);
			}
			$valueRows[] = '(' . implode(', ', $rowValues) . ')';
			$totalRows++;

			if (count($valueRows) >= $chunkSize) {
				$lines[] = 'INSERT INTO ' . $quoteIdentifier($tableName) . ' (' . $columnListSql . ') VALUES';
				$lines[] = '  ' . implode(',' . PHP_EOL . '  ', $valueRows) . ';';
				$valueRows = [];
			}
		}

		if (!empty($valueRows)) {
			$lines[] = 'INSERT INTO ' . $quoteIdentifier($tableName) . ' (' . $columnListSql . ') VALUES';
			$lines[] = '  ' . implode(',' . PHP_EOL . '  ', $valueRows) . ';';
		}

		$lines[] = '';
	}

	$lines[] = 'SET FOREIGN_KEY_CHECKS = 1;';
	$snapshotSql = implode(PHP_EOL, $lines) . PHP_EOL;

	$outputDirectory = dirname($outputPath);
	if (!is_dir($outputDirectory) && !mkdir($outputDirectory, 0775, true) && !is_dir($outputDirectory)) {
		throw new RuntimeException('Unable to create snapshot directory: ' . $outputDirectory);
	}

	if (file_put_contents($outputPath, $snapshotSql) === false) {
		throw new RuntimeException('Unable to write snapshot file: ' . $outputPath);
	}

	echo 'Database snapshot exported to: ' . $outputPath . PHP_EOL;
	echo 'Tables exported: ' . count($tables) . ', rows exported: ' . $totalRows . PHP_EOL;
} catch (Throwable $exception) {
	fwrite(STDERR, 'Database snapshot export failed: ' . $exception->getMessage() . PHP_EOL);
	exit(1);
}
